#ifndef _MAINLOOP_H_
#define _MAINLOOP_H_

#include <stdlib.h>

/*
 * Frame update function
 * Passes back userdata along with update dt
 */
typedef void(*mainloop_update_fn)(void* ud, float dt);

/*
 * Frame render function
 * Passes back userdata along with render dt
 */
typedef void(*mainloop_render_fn)(void* ud, float dt);

/* Mainloop initialization parameters */
typedef struct mainloop_params {
    mainloop_update_fn update_callback;
    mainloop_render_fn render_callback;
    int should_terminate;
    int updates_per_sec;
    void* userdata;
} mainloop_params;

/* Mainloop performance state */
#define MAX_PERF_SAMPLES 256
typedef struct mainloop_perf_data {
    struct mainloop_perf_counter {
        float average;
        float samples[MAX_PERF_SAMPLES];
        size_t cur_sample, num_samples;
    } update, render, total;
} mainloop_perf_data;

/* Mainloop runner */
void mainloop(const mainloop_params* params, mainloop_perf_data* perf_data);

#endif /* ! _MAINLOOP_H_ */

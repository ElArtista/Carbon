#ifndef _TEXT_H_
#define _TEXT_H_

#include <linmath.h>
#include "texture_font.h"
#include "gfx.h"

typedef struct font {
    texture_font* tfont;
    texture_atlas* atlas;
    gfx_image atlas_img;
} font;

typedef struct text_renderer* text_renderer;

typedef enum text_halignment {
    TEXT_HALIGN_LEFT,
    TEXT_HALIGN_CENTER,
    TEXT_HALIGN_RIGHT
} text_halignment;

typedef enum text_valignment {
    TEXT_VALIGN_TOP,
    TEXT_VALIGN_CENTER,
    TEXT_VALIGN_BOTTOM
} text_valignment;

typedef struct text_draw_desc {
    const char* text;
    font* fnt;
    vec3 pos;
    quat rot;
    vec3 scl;
    text_halignment halign;
    text_valignment valign;
    int scr_width;
    int scr_height;
} text_draw_desc;

text_renderer text_renderer_create();
void text_draw(text_renderer tr, text_draw_desc* desc);
void text_renderer_destroy(text_renderer tr);

#endif /* ! _TEXT_H_ */

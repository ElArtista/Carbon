#ifndef _OPENGL_H_
#define _OPENGL_H_

#include <glad/gl.h>

/* Macro to check that given OpenGL extension is available */
#define HAS_OPENGL_EXTENSION(e) GLAD_##e

/* Function loader type */
typedef void* (*opengl_function_loader)(const char*);

/* OpenGL extension loader */
void opengl_load_extensions(opengl_function_loader fnloader);

/* OpenGL error callback type */
typedef void(*opengl_error_fn)(void* userdata, const char* error);

/* OpenGL error handling params */
typedef struct opengl_error_handler_params {
    opengl_error_fn callback;
    void* userdata;
} opengl_error_handler_params;

/* Registers given callback to call when an OpenGL error occurs */
void opengl_register_error_handler(opengl_error_handler_params params);

#endif /* ! _OPENGL_H_ */
